function calculateCartTotal() {
  const cart = getCart();
  return Math.round(
    (cart.reduce(function (acc, item) {
      const itemDetail = items.find(function (i, index) {
        if (item.id == i.id) return true;
      });
      if (!itemDetail) {
        return acc;
      }
      return acc + parseFloat(itemDetail.price) * item.qty;
    }, 0.0) *
      100) /
      100
  );
}

